a = int(1.6)               #整數
print(a)
a = float(50)              #浮點數
print(a)
a = complex(10)            #複數
print(a)
a = bool(0)                #布林值
print(a)
a = str([123])             #字串
print(a)
a = list("123")            #串列
print(a)
a = set("123")             #集合
print(a)
a = dict([[1, 2], [2, 1]]) #字典
print(a)

