a = 0      #整數
print(a)
a = 0.0    #浮點數
print(a)
a = 0 + 0j #複數，也可以寫成 0j
print(a)
a = False  #布林值，真為 True
print(a)
a = "0"    #字串，也可以寫成 '0'
print(a)
a = [0]    #串列
print(a)
a = {0}    #集合
print(a)
a = {0:0}  #字典
print(a)

